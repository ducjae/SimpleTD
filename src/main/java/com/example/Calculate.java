package com.example;
public class Calculate {
	// 더하기 함수 plus
	public int plus(int a, int b){		
	     return a + b;
	}
	
	// 빼기 함수 minus
	public int minus(int a, int b){		
	     return a - b;
	}
	
	// 곱하기 함수 multiply
	public int multiply(int a, int b){		
	     return a * b;
	}
	
	// 나누기 함수 divide
	public int divide(int a, int b){		
	     return a / b;
	}
}